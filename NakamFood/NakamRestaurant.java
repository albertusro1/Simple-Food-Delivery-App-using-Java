    private void alamatRestoAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_alamatRestoAncestorAdded
        // TODO add your handling code here:
        NakamPemesanan s = new NakamPemesanan(opsi);
        NakamCariMakanan M = new NakamCariMakanan(Resto, telp);
        String AlamatResto = s.getPemesanan();
        if (M.getPemesanan() != null) {
            AlamatResto = M.getPemesanan();
        }
        try {
            // to do preepared statement
            String sql = "SELECT alamat FROM restoran where nama_tempat = '" + AlamatResto + "'";
            java.sql.Connection conn = (Connection) konfig.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);

            while (res.next()) {
                String name = res.getString("alamat");
                alamatResto.setText(name);

            }

        } catch (SQLException e) {
            System.out.println("Error : " + e.getMessage());
        }
    }//GEN-LAST:event_alamatRestoAncestorAdded

