    private void Menu() {

        NakamPemesanan s = new NakamPemesanan(opsi);
        NakamCariMakanan M = new NakamCariMakanan(Resto, telp);
        DefaultTableModel barisTabelMenu = (DefaultTableModel) tabelDftrMenu.getModel();
        // inisialisasi nama resto
        barisTabelMenu.setRowCount(0);
        String NamaRestoran = s.getPemesanan();
        if (M.getPemesanan() != null) {
            NamaRestoran = M.getPemesanan();
        }
        if (Resto == null) {
            Resto = NamaRestoran;
        }
//        System.out.println("restonya = " + Resto);
        try {
            // prepared statement
            // ambil id produk 
            String sql = "SELECT id_produk, deskripsi,harga FROM produk where nama_tempat = '" + NamaRestoran + "'";

            java.sql.Connection conn = (Connection) konfig.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);

            // nomor
            Integer no = 1;
            while (res.next()) {
                // tambah id ke arraylist
                idProd.add(Integer.parseInt(res.getString(1)));
                cntProd.add(0);
                namaProd.add(res.getString(2));
                // untuk tampilin
                barisTabelMenu.addRow(new Object[]{no++, res.getString(2), res.getString(3)});

            }

            NamaResto.setText(NamaRestoran);

        } catch (SQLException e) {
            System.out.println("Error : " + e.getMessage());
        }

    }

