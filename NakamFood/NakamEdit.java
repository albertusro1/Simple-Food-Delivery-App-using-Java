    String namaDepan, inisialTengah, namaBelakang, surel;
    
    private void setField() {
        try {
            conn = DriverManager.getConnection(url);
            String query = "SELECT * FROM pelanggan where telepon_pelanggan like ?";
            st = conn.prepareStatement(query);

            st.setString(1, noTelp);
            rs = st.executeQuery();
            while (rs.next()) {
                surel = rs.getString("surel");
                jtxtSurel.setText(surel);
                namaBelakang = rs.getString("nama_belakang");
                jtxtNamaBelakang.setText(namaBelakang);
                inisialTengah = rs.getString("inisial_tengah");
                jtxtInisialTengah.setText(inisialTengah);
                namaDepan = rs.getString("nama_depan");
                jtxtNamaDepan.setText(namaDepan);
                jtxtSurel.setVisible(true);
                jtxtSurel.repaint();
                jtxtSurel.validate();
            }
        } catch (SQLException ex) {
            Logger.getLogger(NakamEdit.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

