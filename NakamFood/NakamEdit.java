    private void jbtOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtOkActionPerformed
        // TODO add your handling code here:
        namaDepan = jtxtNamaDepan.getText();
        inisialTengah = jtxtInisialTengah.getText();
        namaBelakang = jtxtNamaBelakang.getText();
        surel = jtxtSurel.getText();

        int log = 0;

        try {
            conn = DriverManager.getConnection(url);
            String query = "UPDATE pelanggan SET nama_depan = ?, inisial_tengah = ?, nama_belakang = ?, surel = ? WHERE telepon_pelanggan = ?";
            st = conn.prepareStatement(query);

            st.setString(1, namaDepan);
            st.setString(2, inisialTengah);
            st.setString(3, namaBelakang);
            st.setString(4, surel);
            st.setString(5, noTelp);
            if (st.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(null, "Acccount Updated!", "Nakam Food", JOptionPane.INFORMATION_MESSAGE);
                log = 1;
                this.dispose();
            }
            if (log == 1) {
                NakamPelanggan info = new NakamPelanggan(noTelp);
                info.setVisible(true);
            }

        } catch (SQLException ex) {
            Logger.getLogger(NakamRegister.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbtOkActionPerformed

